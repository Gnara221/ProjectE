   P3M1 = 0x00;   P3M0 = 0x00;   //设置为准双向口
   P4M1 = 0x00;   P4M0 = 0x00;   //设置为准双向口
   P5M1 = 0x00;   P5M0 = 0x00;   //设置为准双向口
